            $criteria = $criteria->buildCriteria(); // build Criteria from SpyCmsVersion object
        }

        if ($criteria->containsKey(SpyCmsVersionTableMap::COL_ID_CMS_VERSION) && $criteria->keyContainsValue(SpyCmsVersionTableMap::COL_ID_CMS_VERSION) ) {
            throw new PropelException('Cannot insert a value for auto-increment primary key ('.SpyCmsVersionTableMap::COL_ID_CMS_VERSION.')');
        }


        // Set the correct dbName
        $query = SpyCmsVersionQuery::create()->mergeWith($criteria);

        // use transaction because $criteria could contain info
        // for more than one table (I guess, conceivably)
        return $con->transaction(function () use ($con, $query) {
            return $query->doInsert($con);
        });
    }

